    private static int mailcheck(char[] x) {
        int i, found = 0;
        for (i = 0; i < x.length; i++) {
            if (x[i] == '@') {
                found = 1;
                break;
            }
        }
        if (found == 1) {
            for (i = i + 2; i < x.length; i++) {
                if (x[i] == '.') {
                    return 1;
                }
            }
        }
        return 0;
    }
    private static int contactcheck(char[] x) {
        int flag = 1;
        for (int i = 0; i < x.length; i++) {
            if (!Character.isDigit(x[i])) {
                flag = 0;
            }
        }
        return flag;
    }
