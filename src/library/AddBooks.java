    private static int bookcheck(char[] x) {
        int flag = 1;
        for (int i = 0; i < x.length; i++) {
            if (!Character.isDigit(x[i])) {
                flag = 0;
            }
        }
        return flag;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // when button is pressed get all data from fields and pass it to add book method as an array
        String[] data = new String[5];
        data[0] = jTextField1.getText();
        data[1] = jTextField2.getText();
        data[2] = jTextField3.getText();
        data[3] = jTextField4.getText();
        data[4] = jTextField5.getText();
        if(bookcheck(jTextField1.getText().toCharArray())==0||bookcheck(jTextField5.getText().toCharArray())==0){
             JOptionPane.showMessageDialog(this, "invalid data!");
        }
        else{
            Librarian.addBook(data, true);
            JOptionPane.showMessageDialog(this, "Books added successfully");
            goBack();
        }
    }//GEN-LAST:event_jButton1ActionPerformed



