    private int contactIDCheck(String id, String contact){
        for (int i = 0; i < contact.length(); i++) {
            if (!Character.isDigit(contact.charAt(i))) {
                return 0;
            }
        }
        for (int i = 0; i < id.length(); i++) {
            if (!Character.isDigit(id.charAt(i))) {
                return 0;
            }
        }
        
        return 1;
        
    }
    
    
    private void issueBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueBookBtnActionPerformed
        String [] data = new String[4];
        data[0] = callNoTF.getText();
        data[1] = stduentIDTF.getText();
        data[2] = studentNameTF.getText();
        data[3] = studentContactTF.getText();
        if(contactIDCheck(data[1], data[3])==0){
            JOptionPane.showMessageDialog(this, "Please enter valid data!");
            return;
        }
        int error = Librarian.issueBook(data, false);
        if(error==0){
            JOptionPane.showMessageDialog(this, "Please enter valid data!");
        }else if(error==3){
            JOptionPane.showMessageDialog(this, "Student id already exists with different data!");
        }else{
            JOptionPane.showMessageDialog(this, "Books issued successfully");
            sendBack();
        }
    }//GEN-LAST:event_issueBookBtnActionPerformed

