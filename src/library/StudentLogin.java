    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        Boolean studentFound = false;
        if(checkID(id)==0){
            JOptionPane.showMessageDialog(this, "Invallid ID");
            return;
        }
        for(int j = 0;j<Librarian.getStudentsi();j++){
            if(Librarian.getStudents()[j].s.getId()==Integer.parseInt(id)){
                studentFound = true;
                this.hide();
                this.dispose();
                ViewStudentInformation x = new ViewStudentInformation(Librarian.getStudents()[j].s);
                x.setVisible(true);
            }
        }
        if(!studentFound){
            JOptionPane.showMessageDialog(this, "Invallid ID");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

